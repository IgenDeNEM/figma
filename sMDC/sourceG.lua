function checkSirenVehicle(veh, model, v1, v2)
  model = model or getElementModel(veh)
  if not v1 then
    v1, v2 = getVehicleVariant(veh)
  end
  if sirenVehicles[model] then
    if sirenVehicles[model].variant then
      for var = 1, #sirenVehicles[model].variant do
        if sirenVehicles[model].variant[var] == v1 then
          return true
        end
      end
    elseif not sirenVehicles[model].variant then
      return true
    end
  end
  return false
end
