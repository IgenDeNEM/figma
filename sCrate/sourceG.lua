function getDetailsForWeight(ct)
  if crateContents[ct] then
    local items = {}
    local itemAmounts = {}
    for i = 1, #crateContents[ct] do
      local item = crateContents[ct][i][1]
      table.insert(items, item)
      if lootAmounts[item] then
        table.insert(itemAmounts, lootAmounts[item][2])
      else
        table.insert(itemAmounts, 1)
      end
    end
    return items, itemAmounts, crateItemNum[ct][2]
  end
end
crateSum = {}
for id in pairs(crateContents) do
  crateSum[id] = 0
  for i = 1, #crateContents[id] do
    crateSum[id] = crateSum[id] + crateContents[id][i][2]
  end
end
function pickItem(id)
  if crateSum[id] then
    local r = crateSum[id] * math.random()
    local c = 0
    for i = 1, #crateContents[id] do
      c = c + crateContents[id][i][2]
      if r <= c then
        return crateContents[id][i][1]
      end
    end
  end
end
