local bankPos = {}
for i = 1, #atmDatas do
  if atmDatas[i].skin then
    table.insert(bankPos, atmDatas[i].pos)
  end
end
function checkDistance(client, atmId)
  if atmDatas[atmId] and getElementInterior(client) == atmDatas[atmId].pos[5] and getElementDimension(client) == atmDatas[atmId].pos[6] then
    local px, py, pz = getElementPosition(client)
    local x, y, z = atmDatas[atmId].pos[1], atmDatas[atmId].pos[2], atmDatas[atmId].pos[3]
    return getDistanceBetweenPoints3D(x, y, z, px, py, pz) <= (atmDatas[atmId].skin and 2.65 or 1.2)
  end
  return false
end
function isInBank(player)
  for i = 1, #bankPos do
    local x, y, z = unpack(bankPos[i])
    local px, py, pz = getElementPosition(player)
    if getDistanceBetweenPoints3D(x, y, z, px, py, pz) <= 3 then
      return true
    end
  end
  return false
end
function checkDistanceEx(px, py, pz, int, dim, atmId)
