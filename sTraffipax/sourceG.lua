function isBike(veh)
  return getVehicleType(veh) == "Bike" or getVehicleType(veh) == "Quad" or getVehicleType(veh) == "BMX"
end
function isCheckedVehicle(veh)
  local sirenState = getVehicleSirensOn(veh)
  if not sirenState then
    return getVehicleType(veh) == "Automobile" or getVehicleType(veh) == "Bike" or getVehicleType(veh) == "Quad"
  else
    return false
  end
end
