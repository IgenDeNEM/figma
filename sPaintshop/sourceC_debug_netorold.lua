	fosos = coroutine.create(function()
		local biggestSync = coordToSync(1023, 1023)
		local counter = 0
		for k, v in pairs(paintjobFiles) do
			counter = counter + 1
			local faszTexture = dxCreateTexture(v)
			
			outputChatBox("KEZDŐDIK: " .. v)
			local name = v:gsub(".png", "")
			local sightFile = fileCreate(name .. ".sight")
			
			local tab = {}
			local pixels = dxGetTexturePixels(faszTexture)
			
			local batchSize = 10240
			local nextYield = batchSize
			
			for i = 1, biggestSync do
				local x, y = syncToCoord(i)
				local color = dxGetPixelColor(pixels, x, y)
				tab[i] = color or 255
				
				if i >= nextYield then
					nextYield = i + batchSize
					setTimer(function()
						coroutine.resume(fosos)
					end, 1, 1)
					coroutine.yield()
				end
			end
			outputChatBox(counter .. " járok")
			fileWrite(sightFile, toJSON(tab, true))
			fileClose(sightFile)
			
			destroyElement(faszTexture)
			
			outputChatBox("KÉSZ: " .. name)
		end
	end)
	
	coroutine.resume(fosos)
	
	addEvent("baszas", true)
	function baszas(pixels, lofasz)
		if not lofasz1 then
			lofasz1 = lofasz
		end
		texture = dxCreateTexture(1024, 1024)
		local cache2 = {}
		local pixels3 = dxGetTexturePixels (texture)
		local counter = 0
		for k, v in pairs(lofasz1) do
			local x, y = syncToCoord(k)
			table.insert(cache2, k, {x, y})
			dxSetPixelColor(pixels3, x, y, v, v, v)
			if v < 255 then
				counter = counter + v
			end
		end
		outputChatBox("Csiszolandó: " .. counter)
		
		texture2 = dxCreateTexture(1024, 1024)
		local pixels2 = dxGetTexturePixels (texture2)
		local counter2 = 0
		for k, v in pairs(pixels) do
			local x, y = syncToCoord(k)
			dxSetPixelColor(pixels2, x, y, v, v, v)
			if cache2[k] then
				counter2 = counter2 + 255
			end
		end
		dxSetTexturePixels(texture2, pixels2)
		
		outputChatBox("Csiszolt: " .. counter2)
		dxSetTexturePixels(texture, pixels3)
	end
	addEventHandler("baszas", root, baszas)
	
